# 4. Polymorphism: Operator Overloading
# When the same operator is allowed to have different meaning according to the context

print( 1 + 2)                # adds 2 numbers: 3
print("sradhya" + "college") # concatenates string
print([1,2,3] + [4,5,6])      # merge 2 lists

# Here "+" have different meanings

# Operators & Dunder Function
# a + b : addition   a _ _add_ _ (b)
# a - b : addition   a _ _sub_ _ (b)
# a * b : addition   a _ _mul_ _ (b)
# a / b : addition   a _ _truediv_ _ (b)
# a % b : addition   a _ _mod_ _ (b)